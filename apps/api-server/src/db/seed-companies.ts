async function seedCompaniesData(companies: Company[]): Promise<string[]> {
  console.log("üì¶ Seeding companies...");
  const ids: string[] = [];

  for (const company of companies) {
    try {
      const existing = await companyQueries.findByName(company.name);
      if (existing) {
        // Update existing company with all fields
        console.log(`  üîÑ Updating company: ${company.name}`);
        await companyQueries.update(existing.id, company);
        ids.push(existing.id);
      } else {
        const created = await companyQueries.createWithId(company);
        console.log(`  ‚úÖ Created company: ${created.name} (${company.city}, ${company.countryCode})`);
        ids.push(created.id);
      }
    } catch (error) {
      console.error(`  ‚ùå Failed to create/update company ${company.name}:`, error);
    }
  }

  return ids;
}

